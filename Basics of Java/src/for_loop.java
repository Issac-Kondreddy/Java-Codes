import java.util.Scanner;

public class for_loop {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        //for  - executes for limited time
        for(int i=10;i>0;i--){
            System.out.print (i + " ");
        }
        System.out.println();
        System.out.println("Happy Birthday!!");
    }
}
