public class expressions {
    public static void main(String[] args) {
        //expression = operands & operators
        //operands = values, variblaes, numbers, quantity
        //operators = + - * / %

        int friends = 10;
        friends = friends +1;
        System.out.println(friends);

        friends = friends - 1;
        System.out.println(friends);

        friends = friends * 2;
        System.out.println(friends);

        friends = friends / 2; //Integer division
        System.out.println(friends);

        friends = friends % 2;
        System.out.println(friends);

        //short hand
        friends += 2;
        System.out.println(friends);

        friends  *= 8;
        System.out.println(friends);

    }
}
